function renderMenuItems() {
    const menuGrid = document.getElementById('menuGrid');
    menuGrid.innerHTML = '';

    const filteredItems = currentFilter === 'all' 
        ? menuItems 
        : menuItems.filter(item => item.category === currentFilter);

    filteredItems.forEach(item => {
        const card = document.createElement('div');
        card.className = 'menu-card';
        card.setAttribute('data-aos', 'fade-up');
        
        card.innerHTML = `
            <img src="${item.image}" alt="${item.name}" class="menu-card-image" onerror="this.src='https://via.placeholder.com/250x200?text=${encodeURIComponent(item.name)}'">
            <div class="menu-card-content">
                <h4 class="menu-card-name">${item.name}</h4>
                <p class="menu-card-category">${getCategoryName(item.category)}</p>
                <div class="menu-card-footer">
                    <span class="menu-card-price">${formatPrice(item.price)}</span>
                    <button class="menu-card-btn" onclick="addToCart(${item.id})">
                        <i class="fas fa-plus"></i> Thêm
                    </button>
                </div>
            </div>
        `;
        menuGrid.appendChild(card);
    });

    if (window.AOS) {
        AOS.refresh();
    }
}

